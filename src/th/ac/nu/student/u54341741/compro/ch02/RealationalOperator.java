public class RealationalOperator {
    
    public static void main(String[] args) {
        int a = 0, b = 1;
        System.out.println(a == b);   // false
        System.out.println(a != b);   // true
        System.out.println(a < b);    // true
        System.out.println(a <= b);   // true
        System.out.println(a >= b);   // false
     }    
}
